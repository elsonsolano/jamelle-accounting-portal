    <h1>Privacy Policy</h1>
    <p class="meta">Jamelle 1122 Corporation &mdash; Last updated: April 7, 2026</p>

    <p>
        This Privacy Policy describes how Jamelle 1122 Corporation ("we," "us," or "our") collects, uses,
        and protects information when you interact with our Facebook Messenger Bot ("the Bot") and related
        business tools. By using the Bot, you agree to the practices described in this policy.
    </p>

    <hr>

    <h2>1. Who We Are</h2>
    <p>
        Jamelle 1122 Corporation is a company operating retail branches in Davao City, Philippines.
        We use this Bot as an internal business tool to allow authorized staff to submit bank deposit
        slips electronically via Facebook Messenger for accounting and record-keeping purposes.
    </p>

    <h2>2. Information We Collect</h2>
    <p>When you interact with our Messenger Bot, we may collect the following information:</p>
    <ul>
        <li><strong>Facebook Sender ID</strong> — a unique identifier assigned by Facebook to your Messenger account, used to recognize registered staff.</li>
        <li><strong>Facebook Display Name</strong> — your name as shown on your Facebook profile, used for identification in our records.</li>
        <li><strong>Employee Code</strong> — a code you provide to verify your identity as an authorized staff member.</li>
        <li><strong>Images</strong> — photos of bank deposit slips that you send through the Bot for processing.</li>
        <li><strong>Extracted Deposit Data</strong> — information extracted from deposit slip images, including bank name, account number, deposit amount, date, and reference number.</li>
    </ul>
    <p>
        We do not collect personal financial information beyond what is visible on the deposit slips
        submitted by authorized staff in the course of their employment duties.
    </p>

    <h2>3. How We Use Your Information</h2>
    <p>The information collected is used solely for the following business purposes:</p>
    <ul>
        <li>Verifying the identity of authorized staff members before allowing access to the Bot.</li>
        <li>Processing and recording bank deposit slips in our internal accounting system.</li>
        <li>Creating corresponding entries in our passbook and financial ledger records.</li>
        <li>Allowing accounting and admin staff to review, verify, and correct submitted deposit records.</li>
        <li>Maintaining an audit trail of deposit submissions for internal accountability.</li>
    </ul>
    <p>
        We do not use any collected information for marketing, advertising, or any purpose outside
        of internal business accounting operations.
    </p>

    <h2>4. How We Store Your Information</h2>
    <p>
        All data collected through the Bot is stored securely on our private servers hosted via Railway
        (railway.app). Images of deposit slips are stored in a private file storage system and are not
        publicly accessible. Access to submitted records is restricted to authorized accounting and
        administrative personnel only.
    </p>
    <p>
        We retain deposit slip records and associated data for a minimum of five (5) years in compliance
        with standard accounting and tax record-keeping requirements under Philippine law.
    </p>

    <h2>5. Data Sharing</h2>
    <p>
        We do not sell, trade, or share your personal information with third parties, except as necessary
        to operate our services:
    </p>
    <ul>
        <li><strong>Meta Platforms, Inc.</strong> — as the provider of the Facebook Messenger platform through which the Bot operates, subject to Meta's own Privacy Policy.</li>
        <li><strong>Anthropic, PBC</strong> — deposit slip images are sent to Anthropic's Claude AI API solely for the purpose of automated data extraction. Images are processed per Anthropic's API usage and privacy terms and are not used to train AI models.</li>
        <li><strong>Railway (PurpleNotes, Inc.)</strong> — our cloud hosting provider, which stores application data on secure servers.</li>
    </ul>
    <p>
        We do not share your information with any other third parties without your explicit consent,
        unless required by law or legal process.
    </p>

    <h2>6. Access and Control</h2>
    <p>
        Since this Bot is intended exclusively for authorized Jamelle 1122 Corporation staff members,
        access is controlled through employee code verification. If you are a registered Bot user and
        wish to have your registration information removed, please contact your immediate supervisor
        or the IT/accounting department.
    </p>

    <h2>7. Security</h2>
    <p>
        We implement appropriate technical and organizational measures to protect your information
        against unauthorized access, alteration, disclosure, or destruction. All communications
        between the Bot and our servers are encrypted via HTTPS. Access to our internal systems
        is protected by authentication and role-based access controls.
    </p>

    <h2>8. Children's Privacy</h2>
    <p>
        This Bot is intended for use by adult employees of Jamelle 1122 Corporation only.
        We do not knowingly collect information from individuals under the age of 18.
    </p>

